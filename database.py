# --- دوال الاتصال بقواعد البيانات ---
def connect_mysql(host, database, user, password):
    """
    محاولة الاتصال بقاعدة بيانات MySQL.
    تتوقع هذه الدالة استلام اسم قاعدة البيانات كوسيط.
    """
    conn = None
    try:
        conn = mysql.connector.connect(
            host=host,
            database=database, # سيتم تمرير اسم قاعدة البيانات هنا
            user=user,
            password=password
        )
        if conn.is_connected():
            print(f"تم الاتصال بنجاح بقاعدة بيانات MySQL: {database}")
            return conn
    except Error as e:
        # تحديداً لو خطأ قاعدة البيانات غير موجودة
        if e.errno == mysql.connector.errorcode.ER_BAD_DB_ERROR:
            print(f"خطأ: قاعدة البيانات '{database}' غير موجودة.")
        # تحديداً لو خطأ في اسم المستخدم أو كلمة المرور
        elif e.errno == mysql.connector.errorcode.ER_ACCESS_DENIED_ERROR:
            print(f"خطأ: فشل الوصول للمستخدم '{user}'. تحقق من اسم المستخدم وكلمة المرور.")
        else:
            print(f"حدث خطأ أثناء الاتصال بـ MySQL: {e}")
        return None

def connect_sqlite(db_file):
    """محاولة الاتصال بقاعدة بيانات SQLite3"""
    conn = None
    try:
        conn = sqlite3.connect(db_file)
        print(f"تم الاتصال بنجاح بقاعدة بيانات SQLite: {db_file}")
        return conn
    except sqlite3.Error as e:
        print(f"حدث خطأ أثناء الاتصال بـ SQLite3: {e}")
        return None
